# ELIF Statement
nama = input("Siapa nama kamu? ")

if nama == "Rangga":  # kondisi 1
    print("ini benar kamu Rangga")
elif nama == "Putra":  # kondisi 2
    print("oh kamu Putra")
elif nama == "Pratama":  # kondisi 3
    print("Ternyata Pratama")
else:
    print(f"Siapa namamu tadi? {nama} ya?")
